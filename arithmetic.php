function divide($a, $b) {
	if (is_numeric($a) && is_numeric($b)) {
   		return $a / $b; 
   	} else {
 		return throwErrorMessage("divide", $a, $b);
 	}

}  echo divide(400, 4) . PHP_EOL;


///////////////////////////////////////////////////////////////////////
