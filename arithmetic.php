function multiply($a, $b) {
	if (is_numeric($a) && is_numeric($b)) {
    	return $a * $b; 
    } else {
 		return throwErrorMessage("multiply", $a, $b);
 	}
}	echo multiply(2000, 5) . PHP_EOL;

