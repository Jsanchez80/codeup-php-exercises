function modulus($a, $b) {
	if (is_numeric($a) && is_numeric($b)) {
   		return $a % $b; 
	} else {
 		return throwErrorMessage("modulus", $a, $b);
 	}

}  echo modulus(20, 5) . PHP_EOL;



function throwErrorMessage($a, $b) {
	return "ERROR: Whoa, Whoa, Whoa, Whoa, Whooooaaa! You have to enter a number to perform this function" . PHP_EOL;
}

